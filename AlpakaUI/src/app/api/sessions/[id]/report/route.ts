export async function GET(
  request: NextRequest,
  { params }: { params: Promise<{ id: string }> }
) {
  try {
    const resolvedParams = await params;
    const sessionId = resolvedParams.id;

    // Get session to determine mode
    const sessionResponse = await fetch(`${request.nextUrl.origin}/api/sessions?id=${sessionId}`, {
      headers: {
        Cookie: request.headers.get('cookie') || '',
      },
    });

    if (!sessionResponse.ok) {
      return NextResponse.json(
        { error: 'Session not found' },
        { status: 404 }
      );
    }

    const sessions = await sessionResponse.json();
    if (!sessions || sessions.length === 0) {
      return NextResponse.json(
        { error: 'Session not found' },
        { status: 404 }
      );
    }

    const session = sessions[0];

    // Only generate report for completed sessions
    if (session.status !== 'COMPLETED') {
      return NextResponse.json(
        { error: 'Report is only available for completed sessions' },
        { status: 400 }
      );
    }

    // Generate mock report
    const report = generateMockReport(sessionId, session.mode);

    return NextResponse.json(report);
  } catch (error) {
    logger.error('Error generating report', normalizeError(error));
    return NextResponse.json(
      { error: 'Failed to generate report' },
      { status: 500 }
    );
  }
}
